package Exam_chapter001.Sample001;
public class Exec001 {
    
    //データの宣言と使用
    //Java のデータ型（プリミティブ型、参照型）
    public static void main(String[] args) {

        //プリミティブ型変数
        //データ型として、boolean,byte,char,short,float,long,doubleも含まれる
        int age = 15;

        //参照型変数
        String name = "TANAKA";
        
        //コンソール上に出力
        System.out.println("名前は" + name + "です、年は" + age + "になります。");

    }
}
