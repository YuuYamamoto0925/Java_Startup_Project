package Sample002;
public class Exec002 {

    public static void main(String[] args) {

        int a = 10;
        int b = 5;
        
        System.out.println("足し算：" + a + "+" + b + "=" + (a+b));
        System.out.println("引き算：" + a + "-" + b + "=" + (a-b));
        System.out.println("かけ算：" + a + "×" + b + "=" + (a*b));
        System.out.println("割り算：" + a + "÷" + b + "=" + (a/b));

    }
}
