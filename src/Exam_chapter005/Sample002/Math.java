package Exam_chapter005.Sample002;
public class Math {

    //2つの引数を加算する
    public int add(int a,int b){
        return a+b;
    }

    //3つの引数を加算する
    public int add(int a,int b,int c){
        return a+b+c;
    }

    //4つの引数を加算する
    public int add(int a,int b,int c,int d){
        return a+b+c+d;
    }

}
